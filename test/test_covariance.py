#!/usr/bin/env python3 -m pytest
# -*- coding: utf-8 -*-
########################################################################################################################

import os
import sys

sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), '..')))

########################################################################################################################

import decontamination

import numpy as np

########################################################################################################################

data = np.random.default_rng(seed = 0).random((25_000, 4), np.float32)

########################################################################################################################

def test_covariance_matrix():

    ####################################################################################################################

    cov_matrix = decontamination.Covariance.compute(4, data, ddof = 1)

    cov_matrix_np = np.cov(data.T, ddof = 1)

    ####################################################################################################################

    assert np.allclose(cov_matrix, cov_matrix_np)

    ####################################################################################################################

    cov_matrix = decontamination.Covariance.compute(4, data, ddof = 0)

    cov_matrix_np = np.cov(data.T, ddof = 0)

    ####################################################################################################################

    assert np.allclose(cov_matrix, cov_matrix_np)

########################################################################################################################
