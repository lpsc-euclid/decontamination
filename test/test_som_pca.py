########################################################################################################################

import pytest
import decontamination

import numpy as np

########################################################################################################################

data = np.random.default_rng(seed = 0).random((25_000, 4), np.float32)

########################################################################################################################

som = decontamination.SOM_PCA(4, 4, 4, dtype = np.float32, topology = 'square')

####################################################################################################################

